package com.spring.demo;

public class Marks {

	private float phy;
	private float che;
	private float mat;
	public float getPhy() {
		return phy;
	}
	public void setPhy(float phy) {
		this.phy = phy;
	}
	public float getChe() {
		return che;
	}
	public void setChe(float che) {
		this.che = che;
	}
	public float getMat() {
		return mat;
	}
	public void setMat(float mat) {
		this.mat = mat;
	}
	
}
